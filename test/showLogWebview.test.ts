import test from 'node:test';
import assert from 'node:assert/strict';

import { renderShowLogWebviewHtml } from '../src/showLogWebview';

test('renders a table-like show log webview shell with graph column and inline commit details support', () => {
  const html = renderShowLogWebviewHtml();

  assert.match(html, /<title>Show Log<\/title>/);
  assert.match(html, /class="table-header"/);
  assert.match(html, /class="summary-count" id="summaryCount"/);
  assert.match(html, /class="filter-input" id="filterInput"/);
  assert.match(html, /placeholder="Filter commits"/);
  assert.match(html, /class="filter-clear" id="filterClear"/);
  assert.match(html, /graph-header-label">Graph<\/span>/);
  assert.match(html, /Show All Branches/);
  assert.match(html, /id="showAllBranchesToggle"/);
  assert.match(html, /id="graphResizer"/);
  assert.match(html, /class="commit-row"/);
  assert.match(html, /data-selected="/);
  assert.match(html, /class="graph-cell"/);
  assert.match(html, /class="commit-files-graph"/);
  assert.match(html, /class="commit-files-list"/);
  assert.match(html, /--show-log-ref-branch: #19d60f;/);
  assert.match(html, /--show-log-ref-head: #d62828;/);
  assert.match(html, /--show-log-ref-tag: #f7f300;/);
  assert.match(html, /--show-log-ref-remote: #f6d8a8;/);
  assert.match(html, /\.ref-badge::after/);
  assert.match(html, /\.ref-badge\[data-ref-kind="head"\]/);
  assert.match(html, /\.ref-badge\[data-ref-kind="branch"\]/);
  assert.match(html, /\.ref-badge\[data-ref-kind="remote"\]/);
  assert.match(html, /\.ref-badge\[data-ref-kind="tag"\]/);
  assert.match(html, /font-size: 8\.5px;/);
  assert.match(html, /function renderRefBadge\(ref\)/);
  assert.match(html, /data-ref-kind="' \+ escapeHtml\(ref\.kind\)/);
  assert.match(html, /escapeHtml\(ref\.label\)/);
  assert.match(html, /function renderTopology\(topology\)/);
  assert.match(html, /function renderContinuationTopology\(topology\)/);
  assert.match(html, /const LANE_COLORS = \[/);
  assert.match(html, /const GRAPH_LANE_SPACING = 10;/);
  assert.match(html, /const GRAPH_LEFT_INSET = 8;/);
  assert.match(html, /const GRAPH_RIGHT_PADDING = 6;/);
  assert.match(html, /const width = getGraphContentWidth\(topology\.laneCount, laneSpacing\);/);
  assert.match(html, /function getGraphContentWidth\(laneCount, laneSpacing\)/);
  assert.match(html, /<svg class="graph-svg" width="/);
  assert.match(html, /function applyGraphColumnWidth\(width\)/);
  assert.match(html, /selectedCommitHashes = normalizeSelectedCommitHashes/);
  assert.match(html, /data-compare-base="/);
  assert.match(html, /data-compare-target="/);
  assert.match(html, /data-compare-base="' \+ \(selectedCommitHashes\[0\] === commit\.hash \? 'true' : 'false'\)/);
  assert.match(html, /\.commit-entry\[data-compare-base="true"\]/);
  assert.match(html, /\.commit-entry\[data-compare-target="true"\]/);
  assert.match(html, /function persistUiState\(\)/);
  assert.match(html, /function normalizeSelectedCommitHashes\(value\)/);
  assert.match(html, /function syncSelectedCommitHashes\(commits\)/);
  assert.match(html, /function selectCommit\(commitHash, append\)/);
  assert.match(html, /selectedCommitHashes\.length === 1 && selectedCommitHashes\[0\] === commitHash/);
  assert.match(html, /selectedCommitHashes\.includes\(commitHash\)/);
  assert.match(html, /function getCompareSelectionForCommit\(commitHash\)/);
  assert.match(html, /vscode\.setState\(/);
  assert.match(html, /if \(event\.ctrlKey\)/);
  assert.doesNotMatch(html, /selectCommit\(commitHash, false\)/);
  assert.match(html, /document\.addEventListener\('pointerdown'/);
  assert.match(html, /type: 'toggleShowAllBranches', value: target\.checked/);
  assert.match(html, /function scheduleFilterUpdate\(value\)/);
  assert.match(html, /type: 'setFilterText', value/);
  assert.match(html, /filterInput\?\.addEventListener\('input'/);
  assert.match(html, /filterClear\?\.addEventListener\('click'/);
  assert.match(html, /class="commit-files"/);
  assert.match(html, /id="loadMoreSentinel"/);
  assert.match(html, /IntersectionObserver/);
  assert.match(html, /type: 'loadMore'/);
  assert.match(html, /Open Commit Details/);
  assert.match(html, /compareSelection\s*\?\s*'<button class="context-menu-button" type="button" data-menu-action="compareCommits">Compare<\/button>'\s*:\s*'<button class="context-menu-button" type="button" data-menu-action="openCommitDetails">Open Commit Details<\/button>'/s);
  assert.match(html, /Open Diff/);
  assert.match(html, /Compare with Worktree/);
  assert.match(html, /Copy to Clipboard/);
  assert.match(html, /context-menu-group/);
  assert.match(html, /vscode\.postMessage\(\{ type: 'ready' \}\);/);
  assert.match(html, /type: 'toggleCommit', commitHash/);
  assert.match(html, /data-menu-action="compareCommits"/);
  assert.match(html, /type: 'compareCommits'/);
  assert.match(html, /baseCommitHash: state\.baseCommitHash/);
  assert.match(html, /compareCommitHash: state\.compareCommitHash/);
  assert.match(html, /data-menu-action="openFile"/);
  assert.match(html, /data-menu-action="compareWithWorktree"/);
  assert.match(html, /type: action,\s*commitHash: state\.commitHash,\s*changeId: state\.changeId/s);
  assert.match(html, /type: 'openCommitDetails', commitHash: state\.commitHash/);
  assert.doesNotMatch(html, /id="loadMoreButton"/);
  assert.doesNotMatch(html, /vscode\.postMessage\(\{\s*type: 'openFile',\s*commitHash: fileRow/s);
});
